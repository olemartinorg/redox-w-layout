void matrix_scan_user(void) {
    uint8_t layer = biton32(layer_state);

    switch (layer) {
        case 0:
            set_led_off;
            break;
        case 1:
            set_led_green;
            break;
        case 2:
            set_led_yellow;
            break;
        default:
            break;
    }
};
